        <p>A terceirização é, hoje, uma prática essencial em favor da sustentabilidade e responsabilidade social. O off-site é uma alternativa para quem não deseja arcar com os custos de construção de uma estação de tratamento própria. Deve, no entanto, ser realizada com um fornecedor experiente e confiável.</p>

